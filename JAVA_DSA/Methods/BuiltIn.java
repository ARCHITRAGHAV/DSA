package Methods;

public class BuiltIn {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(10));
        System.out.println(Math.cbrt(125));
        System.out.println(Math.PI);
        System.out.println(Math.abs(-98));
        System.out.println(Math.floor(-7.8));
        System.out.println(Math.ceil(-3.4));
        System.out.println(Math.min(28, -98));
        System.out.println(Math.max(23.9, 88));
        System.out.println(Math.max(Math.max(23, 34), 89));
        System.out.println(Math.max(Math.max(162, 67), Math.max(56, 89)));
        System.out.println(Math.pow(-2.9, 3));
    }
}
