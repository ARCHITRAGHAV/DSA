package Basics;

public class arithmeticoperationsonint {
    public static void main(String[] args) {
        double x = 39;
        double y = 10;
        System.out.println((int) (x + y));
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
    }
}
