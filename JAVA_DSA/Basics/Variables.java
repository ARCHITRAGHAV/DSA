package Basics;

public class Variables {
    public static void main(String[] args) {
        int x = 7;
        System.out.println(x);
        x = x - 2;
        System.out.println(x);
        x = x + 20;
        System.out.println(x);
        x = x - 30;
        System.out.println(x);
        x = x * 10;
        System.out.println(x);
        x /= 2;
        System.out.println(x);
    }
}
